# length function len()
str="aChitkara"
str2="i am a strong person"
print(len(str))

# to upper case
print(str.upper())

# to lower case
print(str.lower())

# strip
print(str.strip("a"))

# rstrip
print(str.rstrip("a"))


# Replace
print(str.replace("a","t"))

#split
print(str2.split(" "))

#capitalize
print(str2.capitalize())

#center
print(str2.center(50))

#count
print(str2.count('a'))

#endswith
print(str2.endswith("person"))

#startswith
print(str2.startswith("i"))

#find
print(str2.find("am"))

#index
print(str2.index("am"))