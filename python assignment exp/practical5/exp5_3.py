my_list = [1, 2, 3]

# Append method
my_list.append(4)
print("List after append:", my_list)

# Insert method
my_list.insert(1, 5)
print("List after insert:", my_list)

# Remove method
my_list.remove(2)
print("List after remove:", my_list)

# Pop method
popped = my_list.pop()
print("Popped element:", popped)
print("List after pop:", my_list)
