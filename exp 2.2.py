
sample_string = "Hello, World!"

print("Original string:", sample_string)

print("Length of the string:", len(sample_string))


print("Uppercase:", sample_string.upper())


print("Lowercase:", sample_string.lower())


print("Capitalized string:", sample_string.capitalize())


print("Number of 'l' in the string:", sample_string.count('l'))


print("Index of 'World' in the string:", sample_string.find('World'))


print("String with 'Hello' replaced by 'Hi':", sample_string.replace('Hello', 'Hi'))


print("Splitting the string by comma:", sample_string.split(','))


whitespace_string = "   Hello, World!   "
print("Stripped whitespace:", whitespace_string.strip())


print("Is 'HelloWorld' alphabetic?", 'HelloWorld'.isalpha())


print("Is '12345' numeric?", '12345'.isnumeric())
