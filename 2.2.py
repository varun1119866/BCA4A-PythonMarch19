# Define a string
my_string = "Hello, World!"

# Built-in functions of strings
print("Original String:", my_string)
print("Length of the string:", len(my_string))
print("Uppercase:", my_string.upper())
print("Lowercase:", my_string.lower())
print("Capitalized:", my_string.capitalize())
print("Titlecased:", my_string.title())
print("Count of 'l':", my_string.count('l'))
print("Index of 'o':", my_string.index('o'))
print("Replace 'World' with 'Python':", my_string.replace('World', 'Python'))
print("Split by comma:", my_string.split(','))
print("Is the string alphanumeric?", my_string.isalnum())
print("Is the string in lowercase?", my_string.islower())
print("Is the string in uppercase?", my_string.isupper())
print("Is the string titlecased?", my_string.istitle())
print("Does the string start with 'Hello'?", my_string.startswith('Hello'))
print("Does the string end with 'World'?", my_string.endswith('World!'))
