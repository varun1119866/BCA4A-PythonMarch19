# Example of list methods
my_list = [1, 2, 3, 4, 5]

# Append method
my_list.append(6)
print("After appending:", my_list)

# Remove method
my_list.remove(3)
print("After removing:", my_list)

# Extend method
my_list.extend([7, 8, 9])
print("After extending:", my_list)

# Count method
count = my_list.count(2)
print("Count of 2:", count)
