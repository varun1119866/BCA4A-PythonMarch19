#WAP to demonstrate Slicing Operations in Strings

print("Name: Ajay Singla")
print("Roll No: 2210997019")

s = "Ajay Singla"
print(s[0:3])
print(s[0:4])
print(s[0:5])
print(s[0:6])
print(s[0:7])
print(s[0:8])
print(s[0:9])
print(s[0:10])
print(s[0:11])

# skipping values
print(s[0:11:2])
print(s[0:11:4])
