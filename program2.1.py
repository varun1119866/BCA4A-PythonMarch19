# Define a string
my_string = "Hello World!"

# Slicing operations
print("Original string : ", my_string)
print(" ")

print("First three characters : ", my_string[0:3])
print(" ")

print("Every second character : ", my_string[::2])
print(" ")

print("Reversed string : ", my_string[::-1])
print(" ")

print("Substring from index 2 to end : ", my_string[2:])
print(" ")

print("Substring from index 0 to 4 : ", my_string[:5])
print(" ")

print("Substring from index 7 to 11 : ", my_string[7:12])
print(" ")

print("Substring from index -5 to end : ", my_string[-5:])
print(" ")

print("Substring from index -13 to -9 : ", my_string[:-8])
