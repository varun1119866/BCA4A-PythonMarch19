my_string = "Hello, World!"

# Length of the string
print("Length of the string : ", len(my_string))
print(" ")

# Convert the string to uppercase
print("Uppercase : ", my_string.upper())
print(" ")

# Convert the string to lowercase
print("Lowercase : ", my_string.lower())
print(" ")

# Count the occurrences of a specific character
print("Occurrences of 'l' : ", my_string.count('l'))
print(" ")

# Find the index of a specific substring
print("Index of 'World' : ", my_string.find('World'))
print(" ")

# Replace a substring with another substring
new_string = my_string.replace('Hello', 'Hi')
print("Replaced string : ", new_string)
print(" ")

# Split the string into a list based on a delimiter
split_string = my_string.split(',')
print("Split string : ", split_string)
print(" ")

# Check if the string starts with a specific prefix
print("Starts with 'Hello' : ", my_string.startswith('Hello'))
print(" ")

# Check if the string ends with a specific suffix
print("Ends with 'World!' : ", my_string.endswith('World!'))
